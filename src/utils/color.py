    BRIGHT_GREEN = "\033[92m"
    GOLD = "\033[93m"
    SKY_BLUE = "\033[94m"
    PINK = "\033[95m"
    TURQUOISE = "\033[96m"
    WHITE = "\033[97m"

    ORANGE = "\033[38;5;208m"
    CORAL = "\033[38;5;203m"
    LIME = "\033[38;5;118m"
    BROWN = "\033[38;5;130m"

