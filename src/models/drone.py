# +-------------------------------------------------------------------------+
# |                                 Import                                  |
# +-------------------------------------------------------------------------+


from pydantic import BaseModel, Field


# +-------------------------------------------------------------------------+
# |                                  Class                                  |
# +-------------------------------------------------------------------------+

class Drone(BaseModel):

    # +---------------------------------------------------------------------+
    # |                             Attribute                               |
    # +---------------------------------------------------------------------+

    name: str = Field(min_length=1)

    # +---------------------------------------------------------------------+
    # |                             Hashable                                |
    # +---------------------------------------------------------------------+

    def __eq__(self, other):
        if not isinstance(other, Drone):
            return False
        return self.name == other.name

    def __hash__(self):
        return hash(self.name)
